public class Skickar {
    private int id;
    private int köpnr;
    private int produktid;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getKöpnr() {
        return köpnr;
    }

    public void setKöpnr(int köpnr) {
        this.köpnr = köpnr;
    }

    public int getProduktid() {
        return produktid;
    }

    public void setProduktid(int produktid) {
        this.produktid = produktid;
    }
}
